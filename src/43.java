public class MathHomework {
    public static void main(String[] args) {
        // Example code for basic arithmetic operations
        int sum = 5 + 3;
        int difference = 8 - 2;

        System.out.println("The result of the addition is: " + sum);
        System.out.println("The result of the subtraction is: " + difference);

        // Another example with a loop
        for (int i = 0; i < 5; i++) {
            int currentNumber = i * 2;
            System.out.println(currentNumber);
        }
    }
}

public class MathHomework {
    public static void main(String[] args) {
        // Example 1: Addition and subtraction of two numbers
        int num1 = 5;
        int num2 = 3;
        System.out.println("The sum is: " + (num1 + num2));
        System.out.println("The difference is: " + (num1 - num2));
        
        // Example 2: Division and multiplication of two numbers
        double dividend = 10.5;
        double divisor = 3.0;
        System.out.printf("The result of division is: %.2f", (double) (dividend / divisor));
        System.out.println("The product is: " + dividend * divisor);
    }
}
